<h6>En 2002 llegó Reinaldo Rueda. Las cosas no empezaron bien, de los 10 partidos que dirigió Rueda se ganaron 3, se empataron 3 y se perdieron 4. Su último partido lo dirigió frente a Santa Fe (0-0) dejando el equipo en la casilla trece de la tabla. Lo reemplazó Víctor Luna Gómez quien debutó con Once Caldas (2-2) en la ciudad de Medellín en la fecha once del torneo. Asciende del trece hasta el tercer puesto de la tabla en una notable reacción. Medellín había finalizado como tercero en la fase todos contra todos con 35 puntos y le corresponde jugar el cuadrangular “A” con Cali, Bucaramanga y Tolima. El equipo cayó con Bucaramanga en la primera fecha, en el minuto 48’ del segundo tiempo, en un resultado a todas luces injusto. Empató en la segunda jornada con Tolima (1-1) en la capital de la montaña, vence al Deportivo Cali (0-1) en el partido más importante de la serie con un golazo desde 35 metros de Tressor Moreno, en la revancha empatan en Medellín (1-1) y sendos triunfos frente a Bucaramanga (1-0) y Tolima (1-3) le dieron el paso a la final. En el primer juego de la final contra Deportivo Pasto (2-0) no se jugó bien, pero sobraron ganas. Pasto no se encerró y afortunadamente el gol de Medellín llegó rápido y cambió todo. El partido lo aseguró el poderoso mediante un auto golazo de Julio César Valencia a los 20 minutos del segundo tiempo. En el partido de revancha se logró la victoria mediante un golazo de Mauricio Molina de tiro libre a los 28’ y de Walter Escobar a los 57’. Algunos de los artífices de esta gesta heroica fueron, Víctor Luna (DT), David González, Ricardo Calle, Luis Amaranto Perea, Andrés Felipe Orozco, Róbinson Muñoz, Juan Fernando Leal, Edgar Carvajal, John Alexander “El Conejo” Jaramillo, William Vásquez Chacón, Malher Tressor Moreno, David Fernando Montoya, John Javier “Choronta” Restrepo, Mauricio Alejandro Molina, Roberto Carlos “Choto” Cortés, entre otros.</h6>

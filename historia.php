<h6>El 14 de noviembre de 1913 Alberto Uribe Piedrahíta y sus hermanos Luis y Rafael, acompañados por un grupo de paisas provenientes de Familias pudientes, tales como Guillermo Greiffenstein (quien se desempeñó como primer secretario del club) y José Luis Jaramillo, (primer presidente del club) se entusiasmaron con la idea de crear un equipo de fútbol organizado que se enfrentara a Sporting (también llamado Los Extranjeros), un equipo paisa fundado en 1913 por un grupo de antioqueños y europeos, en especial de belgas y suizos, se forjaron iniciativas, se establecieron contactos para el funcionamiento del equipo y se conforma un equipo llamado Medellín FootBall Club.</h6>



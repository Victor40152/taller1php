<h5>Dicha gestión había sido todo un éxito, y el 15 de abril de 1914 se firma el acta inicial de conformación del equipo. En abril de 1914 el equipo enfrentó su primer partido contra Los Extranjeros, el resultado aplastante y devastador para el Medellín fue de 11-0, se pensó en acabar con el equipo ese mismo día, sin embargo, la ilusión con que se había creado permitió su supervivencia. </h5>
